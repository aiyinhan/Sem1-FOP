public class L6Q1 {
    public static void main(String[] args) {
        
        int triangleNumber = 0;
        System.out.println("The triangular numbers are: ");
        for (int i=0 ; i<=20 ; i ++){
            triangleNumber += i ;
            System.out.println("Index: " + i + ", Triangular Number: " + triangleNumber);
        }
    }

}
